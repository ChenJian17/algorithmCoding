class Solution55 {
    public int maxDepth(TreeNode root) {
        if (root == null) return 0;
        Queue<TreeNode> queue = new LinkedList<>();
        queue.add(root);
        int res = 0;
        while(!queue.isEmpty()){
            res++;
            int n = queue.size();
            for(int i =0; i< n; i++){
                TreeNode t = queue.poll();
                if(t.left != null)
                    queue.add(t.left);
                if(t.right != null)
                    queue.add(t.right);
            }

        }
        return res;

    }


}
